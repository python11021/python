# 불 자료형 - True/ False 형태만 가지고 있는 변수
print(f'True {True}')
print(f'False {False}' )
print(f'10 > 0 {10 > 0}')
print(f'10 >= 0 {10 >= 0}')
print(f'10 < 0 {10 < 0}' )
print(f'10 <= 0 {10 <= 0}')
print(f'10 == 0 {10 == 0}')
print(False == 0)

# not 
print(f'not True {not True}')
print(f'not 10 == 0  {not 10 == 0}')